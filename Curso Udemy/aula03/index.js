// String = Texto

console.log('Jorge Azevedo'); // '' - exibir texto
console.log("Jorge Azevedo"); // "" - exibir texto
console.log(`Jorge Azevedo`); // `` - exibir texto

// colocar aspas dentro da outra
console.log("Jorge 'Azevedo' de Souza");
console.log('Jorge "Azevedo" de Souza');

// Template strings
console.log(`Jorge 'Azevedo' de "Souza"`);

// Number = Numeros
console.log(135, 64, 98);
console.log(15.8, 58.6, 66.8); // Pontos flutuantes

// Mescla
console.log(15.8, 'Jorge Azevedo');